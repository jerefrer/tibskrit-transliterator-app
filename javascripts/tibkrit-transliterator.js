var TibkritTransliterator = function(tibetan) {
  return {
    tibetan: tibetan,
    line: '',
    transliterate: function(options = {}) {
      var replaced = removeUntranscribedPunctuationAndNormalize(this.tibetan, true);
      _(replacementMap).each(function(word) {
        var replacement = options.phonetics
          ? word.phonetics || word.transliteration.normalize()
          : word.transliteration
        replaced = replaced.replace(new RegExp('(' + word.tibetan + '[^་།༑༔]*)་ནཱཾ', 'g'), options.phonetics ? '$1་^^^nam' : '$1་^^^nāṁ');
        if (replacement.last() == 'a') {
          replacement = replacement.slice(0, -1);
          replaced = replaced.replace(new RegExp('(' + word.tibetan + ')ཱ་ཡ་', 'g'), options.phonetics ? '$1aya ' : '$1āya ');
          replaced = replaced.replace(new RegExp('(' + word.tibetan + ')་ཡ་', 'g'), '$1aya ');
          replaced = replaced.replace(new RegExp(word.tibetan + '[་།༑༔]', 'g'), replacement + 'a ');
          replaced = replaced.replace(new RegExp(word.tibetan + 'ཿ', 'g'), replacement + (options.phonetics ? 'ah' : 'aḥ'));
        }
        replaced = replaced.replace(new RegExp(word.tibetan, 'g'), replacement);
      })
      this.line = replaced.replace(/་/g, ' ').replace(/ ?\^\^\^/g, '');
      if (options.capitalize) {
        this.line = this.line.capitalize();
        this.line = this.line.replace(/ {2,}(.)/g, (letter) => '    ' + letter.toUpperCase());
      }
      else
        this.line = this.line.replace(/ {2,}/g, '    ');
      return this.line.trim();
    }
  };
}

